void putpixel(int pos_x, int pos_y, unsigned char VGA_COLOR)
{
    unsigned char* location = (unsigned char*)0xA0000 + 320 * pos_y + pos_x;
    *location = VGA_COLOR;
}

void DrawChar(int posx, int posy, int FontChar, char Color){
    for (int y = 0; y < 8; y++) {
        bool Pixels[8];
        VGA_ConvFromByte(VGA_Font[FontChar][y], Pixels);
        for (int x = 0; x < 8; x++) {
            if(Pixels[x]){
                putpixel(posx+x,posy+y,Color);
            }
        }
    }
}

extern void Print(int PosX, int PosY, const char *Strin, char Color){
    const char *String = Strin + 0x0;
    int XOffset = 0;
    while (*String != 0){
        DrawChar(PosX+XOffset,PosY,GetCharVal(*String++),Color);
        XOffset+=8;
    }
}

void DrawRect(int xstart, int ystart, int wid, int hei, unsigned char VGA_COLOR){
    for (int y = 0; y < hei; y++) {
        for (int x = 0; x < wid; x++) {
            putpixel(x + xstart,y + ystart,VGA_COLOR);
        }
    }
}